#include <glad/glad.h>
#include <GLFW/glfw3.h>

#include <glm/glm.hpp>
#include <glm/gtc/matrix_transform.hpp>
#include <glm/gtc/type_ptr.hpp>

// Local Headers
//#include "glitter.hpp"
#include "shader.h"
#include "camera.h"
#include "model.h"
